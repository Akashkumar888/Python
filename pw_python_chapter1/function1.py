
def sum(n1,n2):
    ans=n1+n2
    return ans

def sub(n1,n2):
    ans=n1-n2
    return ans

def mul(n1,n2):
    ans=n1*n2
    return ans

def div(n1,n2):
    ans=n1/n2
    return ans

x=int(input("Enter first number: "))
y=int(input("Enter second number: "))

print(sum(x,y))
print(sub(x,y))
print(mul(x,y))
print(div(x,y))

